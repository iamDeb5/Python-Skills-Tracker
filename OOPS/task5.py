class MathUtils:

    @staticmethod
    def add(x,y):
        return x + y

    @staticmethod
    def subtract(x,y):
        return x - y

    @staticmethod
    def multiply(x,y):
        return x * y

    @staticmethod
    def divide(x,y):
        return x / y

    @staticmethod
    def power(x,y):
        return x ** y

print(MathUtils.add(10,20))
print(MathUtils.subtract(10,20))
print(MathUtils.multiply(10,20))
print(MathUtils.divide(10,20))
print(MathUtils.power(10,20))